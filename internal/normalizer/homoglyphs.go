package normalizer

// defaultHomoglyphs returns a mapping of common Cyrillic and Greek homoglyphs to Latin equivalents.
func defaultHomoglyphs() map[rune]rune {
	return map[rune]rune{
		// Cyrillic → Latin
		'А': 'A', 'а': 'a',
		'В': 'B',
		'С': 'C', 'с': 'c',
		'Е': 'E', 'е': 'e',
		'Н': 'H',
		'І': 'I', 'і': 'i',
		'К': 'K', 'к': 'k',
		'М': 'M',
		'О': 'O', 'о': 'o',
		'Р': 'P', 'р': 'p',
		'Т': 'T',
		'Х': 'X', 'х': 'x',
		'у': 'y',

		// Greek → Latin
		'Α': 'A', 'α': 'a',
		'Β': 'B', 'β': 'b',
		'Ε': 'E', 'ε': 'e',
		'Η': 'H',
		'Ι': 'I', 'ι': 'i',
		'Κ': 'K', 'κ': 'k',
		'Μ': 'M',
		'Ν': 'N', 'ν': 'v',
		'Ο': 'O', 'ο': 'o',
		'Ρ': 'P', 'ρ': 'p',
		'Τ': 'T', 'τ': 't',
		'Υ': 'Y', 'υ': 'u',
		'Χ': 'X', 'χ': 'x',
		'Ζ': 'Z', 'ζ': 'z',

		// Fullwidth → ASCII
		'Ａ': 'A', 'ａ': 'a',
		'Ｂ': 'B', 'ｂ': 'b',
		'Ｃ': 'C', 'ｃ': 'c',
		'Ｄ': 'D', 'ｄ': 'd',
		'Ｅ': 'E', 'ｅ': 'e',
		'Ｆ': 'F', 'ｆ': 'f',
		'Ｇ': 'G', 'ｇ': 'g',
		'Ｈ': 'H', 'ｈ': 'h',
		'Ｉ': 'I', 'ｉ': 'i',
		'Ｊ': 'J', 'ｊ': 'j',
		'Ｋ': 'K', 'ｋ': 'k',
		'Ｌ': 'L', 'ｌ': 'l',
		'Ｍ': 'M', 'ｍ': 'm',
		'Ｎ': 'N', 'ｎ': 'n',
		'Ｏ': 'O', 'ｏ': 'o',
		'Ｐ': 'P', 'ｐ': 'p',
		'Ｑ': 'Q', 'ｑ': 'q',
		'Ｒ': 'R', 'ｒ': 'r',
		'Ｓ': 'S', 'ｓ': 's',
		'Ｔ': 'T', 'ｔ': 't',
		'Ｕ': 'U', 'ｕ': 'u',
		'Ｖ': 'V', 'ｖ': 'v',
		'Ｗ': 'W', 'ｗ': 'w',
		'Ｘ': 'X', 'ｘ': 'x',
		'Ｙ': 'Y', 'ｙ': 'y',
		'Ｚ': 'Z', 'ｚ': 'z',
	}
}
